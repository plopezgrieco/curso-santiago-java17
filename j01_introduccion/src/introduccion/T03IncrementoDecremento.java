package introduccion;

public class T03IncrementoDecremento {
	public static void main(String[] args) {
		int cont = 0;
		
//		cont = cont + 1;
		cont++;
		System.out.println(cont);
		
		int a = 0;
		int b = 0;
		
		System.out.println("a++ " + a++);
		System.out.println("++b " + ++b);
		
		System.out.println(a);
		System.out.println(b);
	}
}
